list=[1,2,3,4,5,6,7]

def sum_list(list):
    sum=0
    for i in list:
        sum=sum+i
    return sum

print("sum of list elements is: ", sum(list))

tuple=(1,2,3,4,5,6,7,8)

def sum_list(tuple):
    sum=0
    for i in tuple:
        sum=sum+tuple[i]
    return sum

print("sum of tuple elements is: ", sum(tuple))

